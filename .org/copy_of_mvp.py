def generate_text(model, processor, filepath, label, detail):
    # Get media type, image/video
    if filepath.endswith(".jpg") or filepath.endswith(".jpeg") or filepath.endswith(".png"):
        media_type = "image"
        # Blip2 processor handles image loading internally, so load the raw image with PIL
        raw_image = Image.open(filepath).convert('RGB')
    # elif filepath.endswith(".mp4") or filepath.endswith(".mov"):
    #     media_type = "video"
    #     # The Qwen model seems to handle video loading internally through process_vision_info
    #     # Blip2 does not have native video support in this function
    #     raise ValueError("Blip2 model does not support video input in this function.")
    else:
        raise ValueError("Unsupported media type or Blip2 does not support video input.")

    # Load prompt based off of the kind of detail requested
    if detail == "condition":
        prompt = "What are the qualities of this welding job?"
    elif detail == "operation type":
        prompt = "What is the operation type of the welding job?"
    elif detail == "sensor modalities":
        prompt = "What are the sensor modalities used in this welding job?"
    else:
        raise ValueError("Unsupported detail type")


    # Prepare the input for Blip2
    inputs = processor(raw_image, text=prompt, return_tensors="pt").to(model.device, torch.float16)

    # Inference: Generation of the output
    generated_ids = model.generate(**inputs, max_new_tokens=128)
    output_text = processor.batch_decode(
        generated_ids, skip_special_tokens=True, clean_up_tokenization_spaces=False
    )
    return output_text

