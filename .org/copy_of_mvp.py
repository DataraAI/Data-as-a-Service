    """
    Generate annotations for BMW egocentric vehicle images using a Vision-Language Model.
    """

    # Detect media type
    filepath_lower = filepath.lower()
    if filepath_lower.endswith((".jpg", ".jpeg", ".png")):
        media_type = "image"
        media = load_image(filepath)
    elif filepath_lower.endswith((".mp4", ".mov")):
        media_type = "video"
        media = load_video(filepath)
    else:
        raise ValueError(f"Unsupported media type for file: {filepath}")

    # Prompt templates for annotation types
    if detail == "viewpoint":
        prompt = (
            "You are an automotive vision model. Describe the camera viewpoint of this "
            "egocentric image of a BMW vehicle. Include the viewing angle (frontal, low-angle, "
            "high-angle, oblique), distance from the grille, symmetry, and any perspective distortion."
        )

    elif detail == "grille_features":
        prompt = (
            "Analyze the BMW kidney grille in the image. Describe its shape, vertical bar count, "
            "bar spacing, chrome/metallic finish, symmetry, and alignment with the hood, headlights, "
            "and bumper. Identify any visible irregularities or defects."
        )

    elif detail == "component_identification":
        prompt = (
            "Identify all major exterior vehicle components visible in this egocentric view. "
            "List parts such as grille, headlights, bumper vents, sensors, hood, air intakes, and "
            "other assemblies. Provide brief descriptions of their visual appearance."
        )

    elif detail == "alignment_quality":
        prompt = (
            "Evaluate the alignment and fit quality of the visible exterior components. "
            "Comment on hood-to-grille alignment, panel gaps, bumper alignment, and symmetry. "
            "List any QC concerns you can visually detect."
        )

    elif detail == "factory_context":
        prompt = (
            "Describe the factory or assembly-line environment around the vehicle. Identify equipment, "
            "tools, conveyor systems, lighting, or robotic machinery visible in the background."
        )

    elif detail == "damage_or_defects":
        prompt = (
            "Inspect the BMW vehicle components for any damage, scratches, dents, misalignment, "
            "missing parts, or manufacturing irregularities. Provide a concise list of issues. "
            "If none are visible, state that clearly."
        )

    else:
        raise ValueError(f"Unsupported detail type: {detail}")

    # Prepare messages for VLM
    messages = [
        {
            "role": "user",
            "content": [
                {
                    "type": media_type,
                    media_type: media,
                },
                {
                    "type": "text",
                    "text": prompt,
                },
            ],
        }
    ]

    # Convert to model-friendly format
    text_prompt = processor.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=True
    )

    image_inputs, video_inputs = process_vision_info(messages)

    inputs = processor(
        text=[text_prompt],
        images=image_inputs,
        videos=video_inputs,
        padding=True,
        return_tensors="pt"
    ).to("cuda")

    # Generate model output
    generated_ids = model.generate(**inputs, max_new_tokens=200)

    # Remove input tokens from output
    trimmed_ids = [
        out[len(inp):]
        for inp, out in zip(inputs.input_ids, generated_ids)
    ]

    # Decode output
    output_text = processor.batch_decode(
        trimmed_ids,
        skip_special_tokens=True,
        clean_up_tokenization_spaces=False
    )

    # Split into lines for convenience
    final_output = []
    for o in output_text:
        final_output.extend(o.split("\n"))

    return final_output

